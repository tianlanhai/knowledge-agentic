#!/bin/bash
# ============================================================================
# 知识库智能体 - 日志输出函数库
# File    : lib/logger.sh
# Purpose : 提供统一的日志格式输出
# ============================================================================

# ============================================================================
# 日志颜色定义
# ============================================================================
readonly COLOR_INFO='\033[32m'      # 绿色
readonly COLOR_SUCCESS='\033[32m'   # 绿色
readonly COLOR_WARN='\033[33m'      # 黄色
readonly COLOR_ERROR='\033[31m'     # 红色
readonly COLOR_RESET='\033[0m'      # 重置

# ============================================================================
# log_info - 输出信息日志
# 参数: $1 = 日志内容
# ============================================================================
log_info() {
    echo -e "${COLOR_INFO}[INFO]${COLOR_RESET} $1"
}

# ============================================================================
# log_success - 输出成功日志
# 参数: $1 = 日志内容
# ============================================================================
log_success() {
    echo -e "${COLOR_SUCCESS}[SUCCESS]${COLOR_RESET} $1"
}

# ============================================================================
# log_warn - 输出警告日志
# 参数: $1 = 日志内容
# ============================================================================
log_warn() {
    echo -e "${COLOR_WARN}[WARN]${COLOR_RESET} $1"
}

# ============================================================================
# log_error - 输出错误日志
# 参数: $1 = 日志内容
# ============================================================================
log_error() {
    echo -e "${COLOR_ERROR}[ERROR]${COLOR_RESET} $1" >&2
}

# ============================================================================
# print_separator - 打印分隔线
# 参数: $1 = 标题（可选）
# ============================================================================
print_separator() {
    echo ""
    echo "========================================"
    if [[ -n "$1" ]]; then
        echo "  $1"
        echo "========================================"
    fi
}

# ============================================================================
# End of file
# ============================================================================
